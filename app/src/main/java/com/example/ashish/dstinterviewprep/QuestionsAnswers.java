public class QuestionsAnswers {
    String questions;
    String Answers;

    public String getQuestions() {
        return questions;
    }

    public String getAnswers() {
        return Answers;
    }

    public QuestionsAnswers(String questions, String answers) {
        this.questions = questions;
        this.Answers = answers;
    }

}
